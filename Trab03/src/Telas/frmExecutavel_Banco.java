    /**
     * Executa criar contas
     *
     * @param evt
     */
    private void btnAddContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddContaActionPerformed
        try {
            int pos = 100;
            if (bc.getLista_de_Contas() != null) {
                pos += bc.getLista_de_Contas().size();
            }
            String op = JOptionPane.showInputDialog(""
                    + "TIPO DE CONTA\n"
                    + "1. Conta Corrente!\n"
                    + "2. Conta Poupanca!");
            if (op.equals("1")) {
                cc = new Conta_Corrente();
                sim = true;
                do {
                    cc.setNrConta(pos);
                    JOptionPane.showMessageDialog(null, "Nr de conta atribuido: " + pos + "!");
                    do {
                        try {
                            double saldoIn = Double.parseDouble(JOptionPane.showInputDialog("Digite o saldo inicial!"));
                            if (saldoIn > 0) {
                                cc.depositar(saldoIn);
                                sim = false;
                            } else {
                                JOptionPane.showMessageDialog(null, "Deve digitar valor positivo!");
                            }
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Erro de digitacao!");
                        }
                    } while (sim);
                    // rel.gerarRelatório(cc);
                    JOptionPane.showMessageDialog(null, "Conta adicionada!");
                    preenche_lbls(cc, null);
                    bc.inserir(cc);
                    pTab.preenche_a_tbl(bc.getLista_de_Contas());
                } while (sim);
            } else if (op.equals("2")) {
                cp = new Conta_Poupaca();
                do {
                    cp.setNrConta(pos);
                    JOptionPane.showMessageDialog(null, "Seu nr de conta eh: " + pos + "!");
                    do {
                        try {
                            double saldoIn = Double.parseDouble(JOptionPane.showInputDialog("Digite o saldo inicial!"));
                            if (saldoIn > 0) {
                                cp.depositar(saldoIn);
                                sim = false;
                            } else {
                                JOptionPane.showMessageDialog(null, "Deve digitar valor positivo!");
                            }
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Erro de digitacao!");
                        }
                    } while (sim);
                    //   rel.gerarRelatório(cp);  
                    preenche_lbls(cp, null);
                    bc.inserir(cp);
                    pTab.preenche_a_tbl(bc.getLista_de_Contas());
                    JOptionPane.showMessageDialog(null, "Conta adicionada!");
                } while (sim);
            } else {
                JOptionPane.showMessageDialog(null, "Opcao invalida!");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAddContaActionPerformed
    /**
     * Exibe dados duma dada conta caso seja encontrada, em labels
     */
    ContaBancaria preenche_lbls(ContaBancaria cont1, ContaBancaria cont2) {
        if (cont1 != null) {
            lblHist1.setText("     Historico: " + cont1.getHistorico());
            lblSaldo1.setText("     Saldo: " + cont1.getSaldo());
            lblnrCont1.setText("     Nr. de conta: " + cont1.getNrConta());
        } else {
            lblHist1.setText("     ---");
            lblSaldo1.setText("     ---");
            lblnrCont1.setText("     ---");
        }
        if (cont2 != null) {
            lblHist2.setText("Historico: " + cont2.getHistorico() + "     ");
            lblSaldo2.setText("Saldo: " + cont2.getSaldo() + "     ");
            lblnrCont2.setText("Nr. de conta: " + cont2.getNrConta() + "     ");
        } else {
            lblHist2.setText("---     ");
            lblSaldo2.setText("---     ");
            lblnrCont2.setText("---     ");
        }
        return cont1;
    }

