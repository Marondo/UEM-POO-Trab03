    /**
     * Deposita numa conta especificada
     *
     * @param evt
     */
    private void btnDepoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepoActionPerformed
        try {
            ContaBancaria c = preenche_lbls(bc.procurarConta(Integer.parseInt(JOptionPane.showInputDialog("Digite o nr da conta:"))), null);
            int i = 0;
            for (ContaBancaria ob : bc.getLista_de_Contas()) {
                if (ob.getNrConta() == c.getNrConta()) {
                    c.depositar(Double.parseDouble(JOptionPane.showInputDialog("Digite o valor a depositar!")));
                    bc.lista_de_Contas.set(i, c);
                    con.cmd_Salva_Conta_Actualizado(bc.caminho, bc.lista_de_Contas);

                    pTab.preenche_a_tbl(bc.getLista_de_Contas());
                    preenche_lbls(c, null);
                    break;
                }
                i++;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro de digitacao!\nDigite inteiros.");
        }
    }//GEN-LAST:event_btnDepoActionPerformed
    /**
     * Levanta um valor de uma dada conata
     *
     * @param evt
     */
    private void btnLevantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLevantActionPerformed
        try {
            ContaBancaria c = preenche_lbls(bc.procurarConta(Integer.parseInt(JOptionPane.showInputDialog("Digite o nr da conta:"))), null);
            int i = 0;
            if (c != null) {
                for (ContaBancaria ob : bc.getLista_de_Contas()) {
                    if (ob.getNrConta() == c.getNrConta()) {
                        if (c.sacar(Double.parseDouble(JOptionPane.showInputDialog("Digite o valor a levantar!"))) == 1) {
                            bc.lista_de_Contas.set(i, c);
                            con.cmd_Salva_Conta_Actualizado(bc.caminho, bc.lista_de_Contas);
                            pTab.preenche_a_tbl(bc.getLista_de_Contas());
                            JOptionPane.showMessageDialog(null, "Levantado com sucesso!");
                        }
                        preenche_lbls(c, null);
                        break;
                    }
                    i++;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro de digitacao!\nDigite inteiros.");
        }
    }//GEN-LAST:event_btnLevantActionPerformed
    /**
     * Faz a transferencia entre duas contas informadas
     *
     * @param evt
     */
    Conexao con = new Conexao();
    private void btnTransfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransfActionPerformed
        try {
            ContaBancaria origem = preenche_lbls(bc.procurarConta(Integer.parseInt(JOptionPane.showInputDialog("Digite o nr da conta - origem:"))), null);
            ContaBancaria destino = bc.procurarConta(Integer.parseInt(JOptionPane.showInputDialog("Digite o nr da conta - destino:")));
            preenche_lbls(origem, destino);
            int or = 0;
            if (origem != null && destino != null) {
                if (origem != destino) {
                    for (ContaBancaria inicio : bc.lista_de_Contas) {
                        if (inicio.getNrConta() == origem.getNrConta()) {
                            int d = 0;
                            for (ContaBancaria fim : bc.lista_de_Contas) {
                                if (fim.getNrConta() == destino.getNrConta()) {
                                    double valor = Double.parseDouble(JOptionPane.showInputDialog("Digite o valor a transferir!"));

                                    origem.transferir(valor, destino);
                                    bc.lista_de_Contas.set(or, origem);
                                    bc.lista_de_Contas.set(d, fim);
                                    con.cmd_Salva_Conta_Actualizado(bc.caminho, bc.lista_de_Contas);
                                    pTab.preenche_a_tbl(bc.getLista_de_Contas());
                                    preenche_lbls(origem, destino);
                                    break;
                                }
                                d++;
                            }
                            break;
                        } else {
                            JOptionPane.showMessageDialog(null, "inicio == origem." + inicio.getNrConta() + "" + origem.getNrConta());
                        }
                        or++;
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Erro! Devem ser contas diferentes.");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro de digitacao!\nDigite inteiros.");
        }
    }//GEN-LAST:event_btnTransfActionPerformed

