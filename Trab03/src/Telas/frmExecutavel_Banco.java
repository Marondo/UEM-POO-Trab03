    /**
     * Habilita ou desabilita o menu superio do sistem baseando se num valor que
     * eh capturado do botaoFINALIZAR
     *
     */
    private void menuCima() {
        if (txt.equalsIgnoreCase(btnFinalizar.getText())) {
            btnDepo.setEnabled(true);
            btnLevant.setEnabled(true);
            btnTransf.setEnabled(true);
            btnRelatorio.setEnabled(true);
            layoutCentral.show(pCard_Central, "pHome");
        } else {
            btnDepo.setEnabled(false);
            btnLevant.setEnabled(false);
            btnTransf.setEnabled(false);
            btnRelatorio.setEnabled(false);
        }
    }

