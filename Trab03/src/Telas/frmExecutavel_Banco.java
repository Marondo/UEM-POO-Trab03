    /**
     * O usuario confirma se deseja sair ou habilitar o menu superior
     *
     * @param evt
     */
    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        preenche_lbls(null, null);
        txt = btnFinalizar.getText();
        try {
            switch (JOptionPane.showInputDialog(""
                    + "1. Sair do Sistema!\n2. Menu"
                    + " de Operacoes!")) {
                case "1": {
                    System.exit(0);
                }
                break;
                case "2": {
                    menuCima();
                    menuBaixo(false);
                }
                break;
                default: {
                    JOptionPane.showMessageDialog(null, "Opcao invalida!");
                }

            }
        } catch (Exception e) {
        }
        txt = "";
    }//GEN-LAST:event_btnFinalizarActionPerformed
